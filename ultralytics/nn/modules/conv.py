class SimAM(nn.Module):
    def __init__(self, lamda=1e-5):
        super().__init__()
        self.lamda = lamda
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        # è·å–è¾“å…¥å¼ é‡çš„å½¢çŠ¶ä¿¡æ¯
        b, c, h, w = x.shape
        # è®¡ç®—åƒç´ ç‚¹æ•°é‡
        n = h * w - 1
        # è®¡ç®—è¾“å…¥å¼ é‡åœ¨é€šé“ç»´åº¦ä¸Šçš„å‡å€¼
        mean = torch.mean(x, dim=[-2,-1], keepdim=True)
        # è®¡ç®—è¾“å…¥å¼ é‡åœ¨é€šé“ç»´åº¦ä¸Šçš„æ–¹å·®
        var = torch.sum(torch.pow((x - mean), 2), dim=[-2, -1], keepdim=True) / n
        # è®¡ç®—ç‰¹å¾å›¾çš„æ¿€æ´»å€¼
        e_t = torch.pow((x - mean), 2) / (4 * (var + self.lamda)) + 0.5
        # ä½¿ç”¨ Sigmoid å‡½æ•°è¿›è¡Œå½’ä¸€åŒ–
        out = self.sigmoid(e_t) * x
        # print('è¿è¡Œåˆ°SimAMäº†')
        return out


class CoTAttention(nn.Module):

    def __init__(self, dim=512, kernel_size=3):
        super().__init__()
        self.dim = dim
        self.kernel_size = kernel_size

        self.key_embed = nn.Sequential(
            nn.Conv2d(dim, dim, kernel_size=kernel_size, padding=kernel_size // 2, groups=4, bias=False),
            nn.BatchNorm2d(dim),
            nn.ReLU()
        )
        self.value_embed = nn.Sequential(
            nn.Conv2d(dim, dim, 1, bias=False),
            nn.BatchNorm2d(dim)
        )

        factor = 4
        self.attention_embed = nn.Sequential(
            nn.Conv2d(2 * dim, 2 * dim // factor, 1, bias=False),
            nn.BatchNorm2d(2 * dim // factor),
            nn.ReLU(),
            nn.Conv2d(2 * dim // factor, kernel_size * kernel_size * dim, 1)
        )

    def forward(self, x):
        bs, c, h, w = x.shape
        k1 = self.key_embed(x)  # bs,c,h,w
        v = self.value_embed(x).view(bs, c, -1)  # bs,c,h,w

        y = torch.cat([k1, x], dim=1)  # bs,2c,h,w
        att = self.attention_embed(y)  # bs,c*k*k,h,w
        att = att.reshape(bs, c, self.kernel_size * self.kernel_size, h, w)
        att = att.mean(2, keepdim=False).view(bs, c, -1)  # bs,c,h*w
        k2 = F.softmax(att, dim=-1) * v
        k2 = k2.view(bs, c, h, w)
        # print('è¿è¡Œåˆ°CoTAttentionäº†')
        return k1 + k2



class Concat(nn.Module):
    """Concatenate a list of tensors along dimension."""

    def __init__(self, dimension=1):
        """Concatenates a list of tensors along a specified dimension."""
        super().__init__()
        self.d = dimension

    def forward(self, x):
        """Forward pass for the YOLOv8 mask Proto module."""
        return torch.cat(x, self.d)
